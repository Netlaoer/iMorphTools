local function BuildEnchantData(slot, prefix)
    local tbl = {}
    local order = {}
    for _, def in ipairs(EnchantDefs) do
        local name = prefix .. def[1]
        local id = def[2]
        tbl[name] = function() SetEnchant(slot, id) end
        table.insert(order, name)
    end
    return tbl, order
end

