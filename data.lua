-- 通用辅助函数：从定义列表自动生成映射表和排序列表
-- 只需修改 Defs 一处即可同时更新表和排序
-- ============================================
local function BuildIDTable(defs)
    local tbl = {}
    local order = {}
    for _, def in ipairs(defs) do
        tbl[def[1]] = def[2]
        table.insert(order, def[1])
    end
    return tbl, order
end

